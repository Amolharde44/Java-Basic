package com.DSA.Pattern;

public class ReverseOrder {
	
	public static void main(String[] args) {
		
		for(int i=1;i<=5;i++)
		{
			System.out.print(i+" ");
		}
		System.out.println();
		for(int j=10;j>=6;j--)
		{
			System.out.print(j+" ");
		}
		System.out.println();
		for(int k=11;k<=15;k++)
		{
			System.out.print(k+" ");
		}
	}

}
